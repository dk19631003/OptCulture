package org.mq.optculture.business.nexon;

public class StoreInfo {
	
	private String storeName;
	private String storeNumber;
	private String storeCode;
	private String storeSaleAmount;
	
	public String getStoreName() {
		return storeName;
	}
	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}
	public String getStoreNumber() {
		return storeNumber;
	}
	public void setStoreNumber(String storeNumber) {
		this.storeNumber = storeNumber;
	}
	public String getStoreCode() {
		return storeCode;
	}
	public void setStoreCode(String storeCode) {
		this.storeCode = storeCode;
	}
	public String getStoreSaleAmount() {
		return storeSaleAmount;
	}
	public void setStoreSaleAmount(String storeSaleAmount) {
		this.storeSaleAmount = storeSaleAmount;
	}

}
