package org.mq.optculture.model;

public class RecommendationResponseObject {
	
	    public String variant_code;
	    public String Department;
	    public String image_url;
	    public String description;
	    
		public String getVariant_code() {
			return variant_code;
		}
		public void setVariant_code(String variant_code) {
			this.variant_code = variant_code;
		}
		public String getDepartment() {
			return Department;
		}
		public void setDepartment(String Department) {
			this.Department = Department;
		}
		public String getImage_url() {
			return image_url;
		}
		public void setImage_url(String image_url) {
			this.image_url = image_url;
		}
		public String getDescription() {
			return description;
		}
		public void setDescription(String description) {
			this.description = description;
		}


}
